<script setup>
import { ref, reactive } from 'vue'
import { useUserStore } from '~/stores/user'

const userStore = useUserStore()
const config = useRuntimeConfig()
const { isDark, toggleDarkMode } = useDarkMode()

const formData = reactive({
  name: '',
  email: '',
  password: '',
  role: 'customer'
})

const isLoading = ref(false)
const errorMessage = ref('')
const successMessage = ref('')

const handleRegister = async () => {
  errorMessage.value = ''
  successMessage.value = ''
  isLoading.value = true

  try {
    const res = await $fetch(`${config.public.baseUrl}/auth/register`, {
      method: 'POST',
      headers: { Authorization: `Bearer ${userStore.token}` },
      body: formData
    })

    if (res.success) {
      successMessage.value = 'Account created successfully!'
      formData.name = ''
      formData.email = ''
      formData.password = ''
      formData.role = 'customer'
    } else {
      errorMessage.value = res.message || 'Registration failed'
    }
  } catch (err) {
    errorMessage.value = err.data?.message || 'An error occurred'
  } finally {
    isLoading.value = false
  }
}

const handleLogout = () => { userStore.logout(); navigateTo('/login') }

definePageMeta({ layout: false })
</script>